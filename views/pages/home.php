        <div class ="col-lg-8 slider-2-text"><h2>What is gleaning and why is it necessary?</h2><br>
          <p class="gleaning-text">According to the <a href="https://vcm-international.com/">VCMI</a> (Value Chain Management International),<br> in 2019, <a href="https://discord.com/channels/1037858044836593704/1038119804701249629/1038119834082361415">13% of fruits and vegetables go unharvested.</a>
            <br><br>Modern agriculture implies modern technology, however the usage of heavy agricultural machinery leads to the waste of a significant portion of harvests.
            Indeed, from products that fell to the ground when the machine passed to the ones that couldn't be collected automatically, notable amounts of products
            are yet to be collected. In some cases, the cost to harvest what's left is significantly higher than the estimated sales revenue. <br><br>This is where the gleaning comes in handy!
             Gleaning consists of groups of individuals (gleaners) who, after getting in contact with the agricultors, come and harvest those products free of charge. At the end of the day,
              the products collected are distributed between 3 actors:  the farmers, a local food bank and the gleaners.
